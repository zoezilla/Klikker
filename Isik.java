public class Isik {

    private String nimi;
    private int skoor;

    public Isik(String nimi, int skoor) {
        this.nimi = nimi;
        this.skoor = skoor;
    }

    public String getNimi() { return nimi; }

    public int getSkoor() { return skoor; }

    public void setSkoor(int skoor) { this.skoor = skoor; }





}
